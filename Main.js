var field = [];

function drawPlayer() {
	fill(255,0,0);
	rect(playerX,playerY,blockSize,blockSize);
	
	playerX += playerXSpeed;
	playerY += playerYSpeed;
	playerYSpeed += 0.1
	
	if (keys[RIGHT]) {
		playerXSpeed += 0.1;
	}
	if (keys[LEFT]) {
		playerXSpeed -= 0.1;
	}
	if (keys[UP] && isOnGround) {
		playerYSpeed -= 3;
	}
	
	
	if (playerYSpeed > 0) {
		var playerXBlock = round(playerX/blockSize);
		var playerYBlock = round(playerY/blockSize);
		
		if (field[playerYBlock+1][playerXBlock] != AIR) {
			playerYSpeed = 0;
			playerY = playerYBlock*blockSize;
			isOnGround = true;
		}
		
	}
	else{
		isOnGround = false;
	}
	
	if (playerXSpeed > 0) {
		var playerXBlock = round((playerX+blockSize/2)/blockSize);
		var playerYBlock = round(playerY/blockSize);
		
		if (field[playerYBlock][playerXBlock] != AIR) {
			playerXSpeed = 0;
			playerX = playerXBlock*blockSize-blockSize;
		}
		
	}
	
	if (playerXSpeed < 0) {
		var playerXBlock = round((playerX-blockSize/2)/blockSize);
		var playerYBlock = round(playerY/blockSize);
		
		if (field[playerYBlock][playerXBlock] != AIR) {
			playerXSpeed = 0;
			playerX = playerXBlock*blockSize+blockSize;
		}
		
	}

	
	
};
function drawBlock(type,x,y) {
	switch(type) {
		case AIR:
			noStroke();
			fill(100,150,255);
			rect(x*blockSize,y*blockSize,blockSize,blockSize);
		break;
		
		case DIRT:

			image(dirt,x*blockSize,y*blockSize,blockSize,blockSize);
		break;
		
		case REDPORTAL:
			if (round(redPortalFrame) == 0) {
				image(redPortal1,x*blockSize,y*blockSize);
			}
			if (round(redPortalFrame) == 1) {
				image(redPortal2,x*blockSize,y*blockSize);
			}
			if (round(redPortalFrame) == 2) {
				image(redPortal3,x*blockSize,y*blockSize);
			}
			if (round(redPortalFrame) == 3) {
				image(redPortal4,x*blockSize,y*blockSize);
			}
			if (redPortalFrame > 3) {
				redPortalFrame = 0;
			}
			
		break;
		
		case STONELIGHT:
			image(stoneLight,x*blockSize,y*blockSize,blockSize,blockSize);
		break;
		
		case GRASS:
			image(grass,x*blockSize,y*blockSize,blockSize,blockSize);
		break;
		

		default:
			fill(255,255,255);
			stroke(255,0,0);
			rect(x*blockSize,y*blockSize,blockSize,blockSize);
		
	}
}
draw = function() {
	background(0,0,0);
	redPortalFrame += 0.02;
	
	field = rooms[room];
	var playerFieldX = round(playerX/blockSize);
	var playerFieldY = round(playerY/blockSize);
	
	for(var i = 0; i < field.length; i += 1) {
		for(var j = 0; j < field[i].length; j += 1) {
			if (dist(playerFieldX,0,j,0) < viewDistance && dist(playerFieldY,0,i,0) < viewDistance) {
				drawBlock(field[i][j],j,i);
			}
		}
	}

	drawPlayer();
};